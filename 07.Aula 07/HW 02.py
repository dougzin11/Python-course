"""
Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês.
"""

ganho_hora = int(input('Digite o quanto você ganha por hora: '))
hora_tabalho = int(input('Digite o número de horas trabalhadas no mês: '))
print('Total do salário no mês referido é R$', ganho_hora*hora_tabalho)